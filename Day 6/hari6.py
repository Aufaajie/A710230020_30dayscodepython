# Program untuk mengonversi suhu dari Celsius ke Fahrenheit

# Meminta input suhu dalam Celsius dari pengguna
suhu_celsius = float(input("Masukkan suhu dalam Celsius: "))

# Mengonversi suhu dari Celsius ke Fahrenheit
suhu_fahrenheit = (suhu_celsius * 9/5) + 32

# Menampilkan hasil konversi
print("Suhu dalam Fahrenheit:", suhu_fahrenheit)
